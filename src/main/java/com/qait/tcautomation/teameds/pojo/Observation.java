package com.qait.tcautomation.teameds.pojo;

public class Observation {

	private String educator;
	private String district;
	private String noOfTimesObserved;
	private String windowName;
	private String districtId;

	public String getEducator() {
		return educator;
	}

	public String getDistrict() {
		return district;
	}

	public String getNoOfTimesObserved() {
		return noOfTimesObserved;
	}

	public String getWindowName() {
		return windowName;
	}

	public String getDistrictId() {
		return districtId;
	}

	public void setEducator(String educator) {
		this.educator = educator;
	}

	public void setDistrict(String district) {
		this.district = district;
	}

	public void setNoOfTimesObserved(String noOfTimesObserved) {
		this.noOfTimesObserved = noOfTimesObserved;
	}

	public void setWindowName(String windowName) {
		this.windowName = windowName;
	}

	public void setDistrictId(String districtId) {
		this.districtId = districtId;
	}

}
